    <h5>For each sub-attribution in a given case, the following data is included:</h5>
    <h5>Who, specifically, carried out the interference?</h5>
    <ul>
        <li><strong>Sub-Actor</strong> (free text). Expressly named Actor included in the primary attribution to whom specific activities are linked.</li>
        <li><strong>Sub-Actor Nation</strong> (free text). National affiliation of the named sub-actor.</li>
        <li><strong>Sub-Actor Parent Organization or Affiliation</strong> (free text). The organization, institution, or affiliation within which the sub-actor operates. This does not include Actor Nation. (Examples would include IRGC, 8200, CENTCOM, Ministry of Public Security.)</li>
        <li><strong>Campaign Tag</strong> (free text). Does this sub-attribution connect to any larger pattern of behavior? See the existing list of "Campaign" Tags before making a new tag.</li>
        <li><strong>Date of Activity</strong> (free text). Date or range of purported activity.</li>
    </ul>

