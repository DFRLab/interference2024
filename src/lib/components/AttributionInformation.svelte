    <ul>
        <li><strong>Short Title</strong> (free text).</li>
        <li><strong>Short Description</strong> (free text).</li>
        <li><strong>Link to Attribution</strong> (link).</li>
    </ul>

    <h5>When did the interference and attribution occur?</h5>
    <ul>
        <li>
            <strong>Date(s) of Activity.</strong> Date or range of purported activity.
            <ul class="inner">
                <li><strong>Start</strong> (date). Input if start date is known; if not, omit.</li>
                <li><strong>End</strong> (date). Input if end date is known, if not, omit.</li>
                <li><strong>Date of Attribution</strong> (date). Date corresponds to date of link of attribution.</li>
            </ul>
        </li>
    </ul>

    <h5>Who is making the attribution, against whom?</h5>
    <ul>
        <li><strong>Source of Allegation</strong> (free text). The original source of the interference allegation.</li>
        <li><strong>Source Nation</strong> (free text). The country where the source of the interference allegation originates. Since the scope of this dataset is interference in the US, the most common source nation for allegations is the United States. The source nation does not necessarily denote the actor was associated with a national government.</li>
        <li>
            <strong>Source Category</strong> (select all that apply).
            <ul class="inner">
                <li><strong>Civil Society Organization.</strong> A nonprofit, non-governmental, non-media entity, typically a university or think tank.</li>
                <li><strong>Foreign Government Body.</strong> A non-US government entity.</li>
                <li><strong>Government.</strong> Government agencies, elected representatives, and officials, even if quoted anonymously.</li>
                <li><strong>Influential Individual.</strong> A noteworthy individual, not currently affiliated with another category, who is deemed nationally recognizable or operating in the public sphere.</li>
                <li><strong>Media.</strong> Only applies if a news organization makes the allegation on the basis of its own investigation. A media organization reporting on an allegation made by someone else (e.g. an anonymous government official) is not included.</li>
                <li><strong>Private Consultancy.</strong> A company engaged in private monitoring and risk consulting, typically in the field of cybersecurity.</li>
                <li><strong>Technology Company.</strong> A company that operates a social media platform or offers a technology service.</li>
            </ul>
        </li>
        <li><strong>Actor</strong> (free text). Brief description of the actor purportedly responsible for the interference attempt.</li>
        <li><strong>Actor Nation</strong> (free text). The country where the interference originates, according to the source. When an allegation comes from a non-state political actor, this field is the nation of origin of that non-state political actor. This does not necessarily denote an actor is associated with the national government.</li>
        <li>
            <strong>Attribution Type</strong> (select all that apply).
            <ul class="inner">
                <li><strong>Direct Attribution.</strong> The source directly accuses the actor of malicious political behavior.</li>
                <li><strong>Proxy/Inferred Attribution.</strong> The source does not make a direct attribution, but clearly states that the activity is likely associated with the actor or strongly implies the accusation is directed at the actor.</li>
                <li><strong>Non-Aligned Commercial Activity.</strong> The interference consists of malicious commercial activity rather than a politically motivated information operation.</li>
            </ul>
        </li>
        <li><strong>Campaign</strong> (free text). An identifying tag used to relate attributions to one another which revolve around the same emergent narratives, tactics, or subjects. Discrete tags indicating the activities were part of a larger pattern of behavior or a concerted effort.</li>
    </ul>

