    <h5>How was the interference purportedly conducted?</h5>
    <ul>
        <li>
            <strong>Method</strong> (select all that apply). Methods used in both the creation and the amplification of content related to the alleged foreign interference.
            <ul class="inner">
                <li><strong>Brigading.</strong> Authentic social media accounts but evidence of coordinated amplification or harassment.</li>
                <li><strong>Sockpuppets.</strong> Inauthentic social media accounts; evidence suggests a high likelihood of human operation.</li>
                <li><strong>Third-Party Automation.</strong> Inauthentic social media accounts; evidence suggests a high likelihood of automation by third-party program.</li>
                <li><strong>DDoS.</strong> Distributed denial-of-service attack; malicious attempt to disrupt server traffic.</li>
                <li><strong>Domain Spoofing.</strong> Manipulation of search queries and results; typosquatting.</li>
                <li><strong>Influencer Payola.</strong> Clandestine or indirect payment to an organization or influential individual for the purposes of content creation or amplification.</li>
                <li>
                    <strong>Hacking</strong> (select all that apply). Unauthorized and clandestine access to or manipulation of digital systems, networks, or data, often for the purpose of information gathering, system disruption, or data manipulation.
                    <ul class="inner-square">
                        <li><strong>Data Manipulation.</strong> The clandestine manipulation of computer systems or accounts; Account hijacking or the cooptation of users' social media profiles.</li>
                        <li><strong>Data Exfiltration.</strong> Unauthorized movement of data; spearphishing; hack-and-release.</li>
                        <li><strong>Other.</strong> Forms of hacking not captured by the categories above.</li>
                    </ul>
                </li>        
                <li><strong>Cheapfakes.</strong> Deceptively edited content; decontextualization of existing media, passed off as current; deceptive co-option of existing brands; does not include use of Generative AI.</li>
                <li><strong>Generative AI.</strong> Augmented or fabricated content produced using artificial intelligence; "deep fakes"; textual generation. Sometimes referred to as "synthetic media," although this term does not adequately distinguish between the use of deep learning and use of more basic manipulative techniques.
            </ul>
        </li>
    
    </ul>

